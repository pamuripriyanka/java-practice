package day2;

/*
 * Local variable: A variable declared inside a method, constructor, or block is called a Local Variable. 
 * Accessible only within that method/block.
 * Must be intialized before use.
 */
public class Localvariable_Example 
{

	public static void main(String[] args) // Main method
	{
		int x=10; //Local variable
		System.out.println(x);
	}

}
