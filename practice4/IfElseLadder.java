package Practice4;

public class IfElseLadder 
{

	public static void main(String[] args) 
	{
		int marks=40;
		
		if(marks>=90)
		{
			System.out.println("A");
		}
		else if(marks>=75)
		{
			System.out.println("B");
		}
		else if(marks>=55)
		{
			System.out.println("C");
		}
		else
		{
			System.out.println("Fail");
		}

	}

}
