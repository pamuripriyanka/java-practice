package Practice4;

public class Switch_Case 
{

	public static void main(String[] args) 
	{
		
		char grade ='E';
		
		switch(grade)
		{
		
		case 'A':
			System.out.println(grade + " - Pass");
			break;
			
		case 'B':
			System.out.println(grade + " - Pass");
			break;
			
		case 'C':
			System.out.println(grade + " - Pass");
			break;
		case 'D':
			System.out.println(grade + " - Pass");
			break;	
		default:
			System.out.println("F - Fail");
		}
		

	}

}

