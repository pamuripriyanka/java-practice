package Practice5;

public class For_SumOfFirst10Numbers 
{

	public static void main(String[] args) 
	{
		// Sum of first 10 numbers
        int sum = 0;

        for (int i = 1; i <= 10; i++) 
        {
            sum = sum + i;
        }

        System.out.println("Sum of first 10 numbers = " + sum);

	}

}
